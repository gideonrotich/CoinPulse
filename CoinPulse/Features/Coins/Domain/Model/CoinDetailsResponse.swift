    let iconURL: String?
    let hasContent: Bool?
    let numberOfCoins: Int?
    let the24HVolume, volumeShare, marketCap, marketCapChange: String?
    let marketCapShare, topCurrencySymbol, topCurrencyMarketCap, topCurrencyDominance: String?
    let coinrankingURL: String?
    
    enum CodingKeys: String, CodingKey {
        case slug, shortname, name
        case iconURL = "iconUrl"
        case hasContent, numberOfCoins
        case the24HVolume = "24hVolume"
        case volumeShare, marketCap, marketCapChange, marketCapShare, topCurrencySymbol, topCurrencyMarketCap, topCurrencyDominance
        case coinrankingURL = "coinrankingUrl"
    }
}
