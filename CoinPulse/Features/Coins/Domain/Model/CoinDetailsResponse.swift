import Foundation

struct CoinDetailsResponse: Codable {
    let status: String?
    let data: DataClassDetail?
}

struct DataClassDetail: Codable {
    let coin: CoinDetail?
}

struct CoinDetail: Codable {
    let uuid, symbol, name, description: String?
    let color: String?
    let iconURL: String?
    let websiteURL: String?
    let links: [Link]?
    let supply: Supply?
    let numberOfMarkets, numberOfExchanges: Int?
    let the24HVolume, marketCap, fullyDilutedMarketCap, price: String?
    let btcPrice: String?
    let priceAt: Int?
    let change: String?
    let rank: Int?
    let sparkline: [String?]?
    let allTimeHigh: AllTimeHigh?
    let coinrankingURL: String?
    let tier: Int?
    let lowVolume: Bool?
    let listedAt: Int?
    let hasContent: Bool?
    let notices: JSONAny?
    let contractAddresses: [JSONAny]?
    let tags: [String]?
    let isWrappedTrustless: Bool?
    let wrappedTo: JSONAny?
    
    enum CodingKeys: String, CodingKey {
        case uuid, symbol, name, description, color
        case iconURL = "iconUrl"
        case websiteURL = "websiteUrl"
        case links, supply, numberOfMarkets, numberOfExchanges
        case the24HVolume = "24hVolume"
        case marketCap, fullyDilutedMarketCap, price, btcPrice, priceAt, change, rank, sparkline, allTimeHigh
        case coinrankingURL = "coinrankingUrl"
        case tier, lowVolume, listedAt, hasContent, notices, contractAddresses, tags, isWrappedTrustless, wrappedTo
    }
}

